#include "libmaths.h"

t_vect	m_delta(float a, float b, float c)
{
	t_vect	sol;

	sol[0] = b * b - 4.0 * a * c;
	if (sol[0] > 0)
	{
		sol[1] = (-b - sqrt(sol[0]) / (2 * a));
		sol[2] = (-b + sqrt(sol[0]) / (2 * a));
	}
	return (sol);
}
